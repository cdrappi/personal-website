<h3>THE DOCUMENT ITSELF</h3>

<ul>
  <li>About four pages long</li>
  <li>Dated March 30, either 315 or 317</li>
  <li>
    "Constantine" starts by mentioning the Blessed Acts of Sylvester (leprosy)
  </li>
  <li>
    He says starting now, the Pope is in charge of the Western Roman Empire
  </li>
  <li>Hold priests & bishops in the same regard as senators & the military</li>
  <li>
    Rationale: It is not correct for an "earthly emperor" to rule over Rome
  </li>
  <li>
    Best quote: "we entreat [everyone]... to come subject to our rule, that no
    one of them in any way be allowed to either break these [decrees], or in any
    way overthrow them"
  </li>
</ul>
