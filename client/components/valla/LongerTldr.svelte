<h3>LONGER TLDR</h3>
<div style="width:650px">
  <p>
    CONSTANTINE THE GREAT (272-337) was the first Christian emperor. Generations
    after his death, the Roman empire fell and the church gained influence &
    territory. Around 760, after they had enough power to pull it off, the
    church forged a document known as the
    <i>Donation of Constantine</i>
    . In it, the emperor signs away control of the Western Roman Empire to the
    church. Apparantly he was very grateful to the pope, who cured him of
    leprosy, and this was his way to say "thanks"
  </p>
  <p>
    In 1439, an Italian priest and humanist named LORENZO VALLA was working
    under the king of Sicily. This king was involved in a power dispute with the
    church. He had Lorenzo analyze the
    <i>Donation</i>
    and Lorenzo proved that it was fake. In fact he found this to be obvious,
    and wrote a doctrine on why. His arguments were:
  </p>
  <ul style="width:550px">
    <li>
      <div class="sub-bullet">LOGIC</div>
      : Constantine wouldn't do this nor did he have the power to
    </li>
    <li>
      <div class="sub-bullet">CHRISTIAN MORALS</div>
      : The bible preaches doing good things for no rewards. A good pope
      wouldn't have accepted half of an empire for curing leprosy
    </li>
    <li>
      <div class="sub-bullet">LACK OF CONFLICT</div>
      : The Roman military would have murdered all of the priests immediately
      since they didn't like Christianity yet
    </li>
    <li>
      <div class="sub-bullet">ANACHRONISM</div>
      : The document was dated in 315, but references Constantinople which was
      still named Byzantium at the time
    </li>
    <li>
      <div class="sub-bullet">MEDICAL</div>
      : You don't become a leper overnight. facts
    </li>
    <li>
      <div class="sub-bullet">LANGUAGE</div>
      : The document's Latin was so shitty, there's no way it was written by an
      emperor. It muddled past/present tenses and used words that no one used at
      the time
    </li>
  </ul>
