<h3>HISTORICAL CONTEXT</h3>
<ul>
  <li>
    Before Constantine (272-337), the citizens of the Roman Empire were pagan,
    following the
    <a href="https://en.wikipedia.org/wiki/Religion_in_ancient_Rome">
      Roman religion
    </a>
    - basically a knock-off of the
    <a href="https://en.wikipedia.org/wiki/Ancient_Greek_religion">
      Greek religion.
    </a>
    (Zeus => Jupiter, Poseidon => Neptune, etc.)
  </li>
  <li>
    In 313, Constantine
    <a href="https://en.wikipedia.org/wiki/Edict_of_Milan">declared</a>
    that it was okay to be a Christian. He was warming up to Christianity
    himself
  </li>
  <li>
    In 325, Constantine
    <a href="https://en.wikipedia.org/wiki/First_Council_of_Nicaea">
      gathered up
    </a>
    a bunch of Christian bishops and they defined what it meant to believe in
    Christianity. This is the
    <a href="https://en.wikipedia.org/wiki/Nicene_Creed">Nicene Creed</a>
    and it is still a thing today
  </li>
  <li>
    Although he proclaimed
    <a
      href="https://en.wikipedia.org/wiki/Constantine_the_Great#Religious_policy">
      to believe
    </a>
    in Christianity in his 40s, Constantine waited to be baptized on his
    deathbed. He thought that if he waited, God would forgive the sins he'd
    inevitably commit during the rest of his reign. Genius
  </li>
  <li>
    <a href="https://en.wikipedia.org/wiki/Pope_Sylvester_I">
      Pope Sylvester I
    </a>
    (?-335) was Pope from 314 until his death, so he overlapped the most with
    Constantine
  </li>
  <li>
    As the church got more extreme, they decided it wasn't okay that Constantine
    was baptized on his deathbed. So, the church
    <a
      href="https://en.wikipedia.org/wiki/Constantine_the_Great#Donation_of_Constantine">
      made up a legend
    </a>
    :
    <p />
    <div style="color:white">&lt;fiction&gt;</div>
    Constantine wanted to pick up a 2nd wife. Pope Sylvester wasn't happy about
    this, and the emperor said I don't care what you think, fuck off. Sylvester
    ran away into the woods. Then Constantine becomes a leper! Sylvester comes
    back from the woods and cures him of leprosy. Soon after, Constantine was
    baptized into Christianity.
    <div style="color:white">&lt;\fiction&gt;</div>
  </li>
  <li>
    ==> FAST FORWARD ==> A roman emperor from 996-1002,
    <a href="https://en.wikipedia.org/wiki/Otto_III,_Holy_Roman_Emperor">
      Otto III
    </a>
    , was one of the first people to dispute the
    <i>Donation</i>
    's legitimacy on record. He churned out a
    <a href="https://en.wikipedia.org/wiki/Gospels_of_Otto_III">Gospel</a>
    that raised suspicions of the document. I couldn't find his gospel online
    and I am a level 9 googler.
  </li>
  <li>
    ==> FAST-FORWARD AGAIN ==> to 1439. The king of Sicily,
    <a href="https://en.wikipedia.org/wiki/Alfonso_V_of_Aragon">Alfonso</a>
    had long tried to usurp Naples from the church. He did this largely through
    bloody war, but also had one of his priests hit the books. Lorenzo Valla
    checked out the
    <i>Donation of Constantine</i>
    and the rest is history, which I will continue to summarize for you in
    <a href="http://www.schoolbytes.com/">schoolbytes</a>
    -like form
  </li>
  <li>
    The printing press was invented around the same year, c. 1440. I assume
    Valla was not first in line. So it took decades for his work to be spread
  </li>
  <li>
    In 1559, the church placed Valla's critique on their index of prohibited
    works
  </li>
  <li>
    From 1588-1607, an Italian cardinal named
    <a href="https://en.wikipedia.org/wiki/Caesar_Baronius">Caesar Baronius</a>
    wrote a 12 volume history of the church called
    <i>Annales Ecclesiastici</i>
    . In it, he admits the
    <i>Donation</i>
    was a forgery. After this, pretty much everyone accepted that the document
    was fake
  </li>
  <li>
    Once they realized the jig was up, the church stopped referring to the
    <i>Donation</i>
    to establish authority. However the church didn't officially admit the
    document was a forgery until the 19th century!
  </li>
</ul>
