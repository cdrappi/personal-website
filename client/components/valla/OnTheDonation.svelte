    <span class="sub-bullet">CONCLUSION</span>
    : Valla believes that a Pope forged the document after the church had more
    power than the government. They used this power to further the myth, as they
    would require emperors to confirm that the document is legit. Valla then
    asks: why do we respect the popes at all if they use all these tricks and
    ploys?
  </p>
