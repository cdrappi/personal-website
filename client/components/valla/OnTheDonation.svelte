<h3>VALLA'S TREATISE</h3>
<div style="width:650px">
  <p>
    Valla makes a ton of good points that would convince anyone of the
    document's forgery. But rather than dismantling the document one step at a
    time, he jumps between arguments. For example, he'll start talking about how
    one develops leprosy, and then out of nowhere go really deep into Latin
    grammar. It's all over the place. For this reason our summary will group
    Valla's main points and ignore the chronological order of his writing
  </p>
  <p>
    Before we get going: note that Valla knew that he was playing with fire by
    writing this... "Unless, perhaps we think the supreme pontiff would bear
    these attacks more patiently than would others."
  </p>
</div>

<div style="padding-top:10px">
  <div class="argument-name">constantine wouldn't have done it</div>
  <ul>
    <li>
      Constantine spent so much time conquering land and building the Roman
      empire. Why go through all the trouble just to give it away?
    </li>
    <li>
      God never told any ruler to give up his government: the only guideline was
      to protect the Hebrew people
    </li>
    <li>Jewish kings didn't even give up their land</li>
    <li>
      The document was written on a piece of paper rather than a bronze plate.
      Who would seriously sign away half of Rome on the back of a napkin?
    </li>
  </ul>
  <div class="argument-name">sylvester wouldn't have accepted</div>
  <ul>
    <li>
      Accepting the Donation would have conflicted with Christian teachings
    </li>
    <li>
      "Freely ye have received, freely
      <a href="https://biblehub.com/matthew/10-8.htm">give</a>
      "
    </li>
    <li>
      When the people planned to make Jesus the king, he fled to the mountains.
      "My kingdom is not of this world; if my kingdom were of this world, then
      my servants would
      <a href="https://biblehub.com/john/18-36.htm">fight</a>
      "
    </li>
    <li>
      Example: Elisha didn't accept reward for curing leprosy of Naaman the
      Syrian
    </li>
    <li>
      If Sylvester did accept, we would have a record of it in writing. After
      all this is a monumental transfer of power. No such record exists
    </li>
  </ul>
  <div class="argument-name">
    a transfer of power would have caused great uproar
  </div>
  <ul>
    <li>
      Constantine's sons would not have accepted this quietly. They lived their
      whole life eager to take over for their father. To find out he gave away
      their power to the church? They'd be furious
    </li>
    <li>
      In 313, Constantine made it "okay" to be a Christian. Meaning, most Romans
      were still not down with Christianity at the time. The people would go
      bananas to hear that they're now under rule of the church TWO YEARS LATER!
    </li>
    <li>
      The military, much like the people, were loyal to Rome. If Constantine
      gave his power to Sylvester, the military would have murdered every single
      bishop and priest they could find. Why would Constantine do this to
      Sylvester if he liked him so much?
    </li>
  </ul>
  <div class="argument-name">reference to the city "constantinople"</div>
  <ul>
    <li>
      The document was dated around 315. It mentions "Constantinople" as one of
      the
      <a href="https://en.wikipedia.org/wiki/Pentarchy">five</a>
      episcopal sees of the Roman Empire
    </li>
    <li>
      The term "Constantinople" hadn't yet been created: the city was called
      Byzantium until 330
    </li>
  </ul>
  <div class="argument-name">
    constantine didn't even have leprosy: that legend was made up
  </div>
  <ul>
    <li>
      Valla consulted physicians: leprosy isn't something you wake up with all
      of a sudden. It develops gradually
    </li>
    <li>
      So the idea that Constantine became a leper overnight because he wanted a
      2nd wife is absurd
    </li>
    <li>
      Even if he did have leprosy, he would've known about it before he was
      elected
      <a href="https://en.wikipedia.org/wiki/Roman_consul">consul</a>
      for the fourth time
    </li>
    <li>
      Every time a person in power became a leper, they secluded themselves.
      Constantine did no such thing
    </li>
  </ul>
  <div class="argument-name">the document was riddled with sloppy latin</div>
  <ul>
    <li>
      It used the past tense like "we decreed" and "we granted". Valla comments:
      "as though it were not being done now, but had been done some other time!"
    </li>
    <li>
      Sometimes it would switch tenses, e.g., "we proclaim" vs. "we have
      proclaimed"
    </li>
    <li>
      Romans never used the term
      <a href="https://en.wikipedia.org/wiki/Satrap">"satraps"</a>
      to refer to their leaders. The document used it multiple times
    </li>
    <li>
      Often the document confuses two similar Latin words. Here's a beautiful
      example: "He uses ... '
      <a href="https://en.wiktionary.org/wiki/concubitor#Latin">concubitores</a>
      ' [translated above, bed-watchers] for '
      <a href="https://en.wiktionary.org/wiki/contubernalis#Latin">
        contubernales
      </a>
      ' [companions or attendents]. 'Concubitores' are literally those who sleep
      together and have intercourse; they must certainly be understood to be
      harlots"
    </li>
    <li>
      Valla makes a subtle argument around the usage of the word "given" (in
      Latin, either "illi" or "ad illum"). The Donation says it was "given" by
      Constantine. However in Latin, documents that had no specific recipient
      could not be "given": this word was restricted for letters delivered by a
      courier.
    </li>
  </ul>
</div>
